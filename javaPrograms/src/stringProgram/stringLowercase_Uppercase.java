package stringProgram;

//Write a Java program to convert all the characters in a string to lowercase/uppercase.

public class stringLowercase_Uppercase {

	public static void main(String[] args) {

		String sentence = "The Quick BroWn FoX!";
		System.out.println(sentence + "\ninto LowerCase : " + sentence.toLowerCase());
		System.out.println(sentence + "\ninto UpperCase : " + sentence.toUpperCase());

	}

}
